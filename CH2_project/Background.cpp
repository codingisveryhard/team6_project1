// 타이틀 메뉴창
int menuDraw()
{
	int x = 56;
	int y = 21;
	gotoxy(x - 2, y);
	cout << "> 게임시작";
	gotoxy(x, y + 1);
	cout << "게임정보";
	gotoxy(x, y + 2);
	cout << "  종료  ";
	gotoxy(x - 26, y + 12);
	cout << "※w, s 를 눌러 위, 아래로 움직이고 스페이스 바를 눌러 선택해주세요.※";
	gotoxy(x - 1, y);
	while (1) {
		int n = keyControl();
		switch (n) {
		case UP:
			if (y > 21) {
				gotoxy(x - 2, y);
				cout << " ";
				y--;
				gotoxy(x - 2, y);
				cout << ">";
			}
			break;
		case DOWN:
			if (y < 23) {
				gotoxy(x - 2, y);
				cout << " ";
				y++;
				gotoxy(x - 2, y);
				cout << ">";
			}
			break;
		case SUBMIT:
			return y - 11;
		default:
			break;
		}
	}

	return 0;
}

// 전투 후 메뉴창
int battleMenu()
{
	int x = 35;
	int y = 15;
	printScript(x, y, "> 상점으로 가기");
	y++;
	printScript(x, y, "  다음으로 가기");
	y = 15;
	while (1) {
		int n = keyControl();
		switch (n) {
		case UP:
			if (y > 15) {
				gotoxy(x, y);
				cout << " ";
				y-=2;
				gotoxy(x, y);
				cout << ">";
			}
			break;
		case DOWN:
			if (y < 16) {
				gotoxy(x, y);
				cout << " ";
				y+=2;
				gotoxy(x, y);
				cout << ">";
			}
			break;
		case SUBMIT:
			return y - 15;
		default:
			break;
		}
	}

	return 0;
}
